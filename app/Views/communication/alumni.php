<script type="text/javascript">

function alumni(){

	alert('submitting');
	event.preventDefault();

	$("#ajax").css("visibility", "visible");
	var fname = $("#fname").val();
	var lname = $("#lname").val();
	var gender = $('#gender').val();
	var dob = $('#dob').val();
	var email = $("#email").val();
	var contact_number = $("#contact_number").val();
	var address = $("#address").val();
	var passing_year = $("#passing_year").val();
	var degree = $("#degree").val();
 var subject = $('#subject').val();
 var employee_type = $('#employee_type').val();
 var organization = $('#organization').val();
 var designation =$('#designation').val();
	$.post("http://localhost/dbj/public/college/alumni",{	
		fname:fname,
		lname:lname,
		gender:gender,
		dob:dob,
		email:email,
		contact_number:contact_number,
		address:address,
		passing_year:passing_year,
		degree:degree,
		subject:subject,
		employee_type:employee_type,
		organization:organization,
		designation:designation
		},function(data, status){				  
				  try {
					  var res=JSON.parse(data);
					  } catch (e) {					  
					  }					  
				  if(res["status"]){
					  alert(" success");
					  Swal.fire(
							  'Thank You!',
							  res["message"],
							  'success'
							);
					  $("#ms").css("display", "block");
					  $("#mf").css("display", "none");
					  $("#ajax").css("visibility", "hidden");
					  document.getElementById('cf').reset();
				  }else{
					  $("#mf").css("display", "block");
					  $("#ms").css("display", "none");
					  $("#ajax").css("visibility", "hidden");
				  }
			  });
	
	return false;
}

</script>


