									<strong>Lets start by asking Why Six Sigma?</strong>
									<p style="text-align: justify;">Currently the world is facing a pandemic COVID-19 and every
										country has turned it's 'Fire- fighting' switch ON. Health
										organizations are hustling with patients, Scientists are
										burdened with a tremendous pressure of finding a vaccine,
										Governments are busy trying to control the economy while the
										lay Jack is stuck at home hoping and praying that everything
										gets stabilized soon. Sounds familiar in the corporate world?
										Well it may not always be a pandemic which breaks down your
										organization's machinery, but it could certainly be the
										performance of your product or service or your employees. It
										is a pandemic kind of situation at work-place when either of
										the above fail. Operations have come to a halt, Strategy tries
										fire-fighting through an approach which not everyone is aware
										of, Management is losing it's patience and the Big Boss is
										soon expected to come downstairs with Thor's mighty hammer.</p>
									<p style="text-align: justify;">Now that is a situation, we all would like to avoid. So what
										do we do? Monday morning pep talk, Team building meetings,
										Strategy planning, Work late hours, etc etc. But all this
										could have been avoided at the very start. In the words of
										Benjamin Franklin, "For want of a nail the shoe was lost. For
										want of a shoe the horse was lost. For want of a horse the
										rider was lost. For want of a rider the message was lost. For
										want of a message the battle was lost. For want of a battle
										the kingdom was lost. And all for the want of a horseshoe
										nail"</p>

									<br /> <strong>So what exactly is Six Sigma?</strong>
									<p style="text-align: justify;">This is where Six Sigma comes in. Six Sigma is a data-driven
										problem-solving methodology. The prime focus is on process
										improvement by reducing variations by emphasizing on customer
										satisfaction. Now many may assume it to be no different from
										Quality management, PDCA and other problem solving techniques.
										But that's not the case. Although the values of Six Sigma does
										align to that of the former, the approach is what
										differentiates itself to all the others. It follows a
										structured DMAIC approach which assists the Fire-Fighting team
										to get to the root cause of the issue, amend the process, test
										the solution and then control errors from occurring in future
										in order to meet customers needs and set a new Target for the
										product or process.</p>
									<br /> <strong>How do I know my target?</strong>
									<p style="text-align: justify;">Six Sigma processes aim to meet the top level quality needs
										of the customer with an effectiveness level of 99.99967 %.
										This means a Six Sigma process would produce merely 3.4
										defects per million opportunities or less as a result.
										99.99967 or 99.5% how does it really matter? Well 99.5% would
										signify 100 flights cancelled in a day while 99.99967%
										signifies 3 flights cancelled in 2 months! Six Sigma focusses
										on a standard structured approach which aims to reduce
										variations in processes and generate a defect free service for
										customers.</p>
									<br /> <strong>DMAIC framework</strong>
									<p style="text-align: justify;">There are five stages to perform a Six Sigma project</p>
									<br /> <strong>1. DEFINE</strong>
									<ul>
										<li>Define what exactly is the problem faced by your
											organizations stakeholders</li>
										<li>Know your voice of the customer</li>
									</ul>
									<br /> <strong>1. DEFINE</strong>
									<ul>
										<li>Define what exactly is the problem faced by your
											organizations stakeholders</li>
										<li>Know your voice of the customer</li>
									</ul>
									<br /> <strong>2. MEASURE</strong>
									<ul>
										<li>Measure our existing processes</li>
										<li>How far behind are we from achieving our target?</li>
									</ul>
									<br /> <strong>3. ANALYZE</strong>
									<ul>
										<li>Analyze and identify gaps in the process</li>
										<li>Can these be filled?</li>
										<li>Can we standardize the process or reduce variability?</li>
									</ul>
									<br /> <strong>4. IMPROVE</strong>
									<ul>
										<li>Strategize the solution</li>
										<li>Brainstorm with all stakeholders</li>
										<li>Have a pilot run for the solution</li>
									</ul>
									<br /> <strong>5. CONTROL</strong>
									<ul>
										<li>Monitor the improved process</li>
										<li>Document the findings</li>
										<li>Control the process and avoid future mishaps from taking place</li>
									</ul>
									<br /> <strong>Why Lean Six Sigma is gaining importance in the
										present times?</strong>
									<p style="text-align: justify;">The corporate environment today is quite dynamic. Band aid
										solutions no longer work in the age of PULL organizations
										where management is more Precautionary rather than
										Reactionary. Several small, medium, and large organizations
										today use Six Sigma techniques knowingly or unknowingly. It is
										the one of the only forms of Quality management tools which
										when used systematically, can bring about ginormous
										proportions of improvement in the organization. Six Sigma
										experts who are usually graded as Yellow, Green, Black, or
										Master Black belts, are those quality leaders who can help the
										organization take enormous leaps rather than incremental
										spikes. Since its inception, Six Sigma has gained tremendous
										popularity in industries such as of Manufacturing, Finance,
										Banking, Healthcare, Logistics, Automobiles,
										Telecommunication, BPO, Information Technology, etc.</p>
									<br /> <strong>What do I get out of it?</strong>
									<p style="text-align: justify;">Learning Six Sigma and applying its methodologies can have
										incredible impact on your career. Being able to put Six Sigma
										certification into your profile shows your promise to
										improving your business's understanding and analytical
										abilities. Six Sigma certification gives you that high
										professional pedestal to stand out from the competition. This
										can lead to better opportunities, better salaries, and most
										importantly streamlines your thought process as a Leader.</p>
