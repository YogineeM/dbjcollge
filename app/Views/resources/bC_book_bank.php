                                            <p style="font-size:16px">
                                               <ul class="list-unstyled list-services" style="padding-left:40px; font-size:14px">
                                <li> <i class="fa fa-chevron-circle-right color-gray-light"></i>1.	It is book bank for the poor and needy students, which helps them to use the required books for a nominal charge.</li>   
                                <li> <i class="fa fa-chevron-circle-right color-gray-light"></i>2.	Students get to use their books for the entire year /semester.</li>
                            <li><i class="fa fa-chevron-circle-right color-gray-light"></i>3.	The amount collected under the scheme is submitted to the office for the next years’ collection.</li>
                            </ul>
                            Under this scheme, books are distributed to the poor and needy students from senior college.<br>
                            This year Rs. 1,42, 622/- are expended for the purchase of new books in this scheme, and total 880 new textbooks are purchased from this amount. Till the end of January 2018, 1755 books were distributed to 611 students. These books were distributed to the needy students for the period of a year, against the 20% of the book price. An amount of Rs. 52000/- is collected from this scheme and deposited to Students Welfare Scheme of the college.
