					<h2>Future Plans</h2>
					<ul class="list-unstyled list-services" style="padding-left: 40px">
						<li><h4>Extension of Library Building :-</h4>
							<p style="font-size: 16px">Due to increasing number of students,
								the present library space is insufficient. So, in the Library
								Committee meeting the Principal announced the plan of extension
								the library building within a year. By this plan, the library
								would have its own multi-storied building, having full-fledge
								reading hall with cozy and sufficient number of furniture. Thus,
								the capacity of Reading Hall will increase up to 300 students at
								a time.</p></li>
						<li><h4>Library services with help of Information Technology to
								Teachers and Students:-</h4>
							<p style="font-size: 16px">With the help of modern information
								Technology,i.e. Internet, it will be very easy to reach up to
								any information, and knowledge. So, library will have separate
								internet connection with modern amenities, like CD drives,
								scanners, reprographic machines etc. With the help of internet,
								our library can search any database of any library in the
								country and the world by accessing their database like PUNENET,
								DELNET , catalogue of Library of Congress (USA).The various
								departments of our college can also search our database by using
								LAN Network. Thus they can save their study and research time.
								We are also thinking to give such services to our students so
								that they will also get the latest information in their field of
								interest.</p></li>
						<li><h4>Use of RFID system in the library :-</h4>
							<p style="font-size: 16px">To avoid misplacement and misuse of
								library books, library is planning to adopt the RFID technology.</p></li>
						<li><h4>Masters Course in Library and Information Science :-</h4>
							<p style="font-size: 16px">We are planning to start a Master
								Degree Course in the Library and Information Science (M.Lib.
								I.Sc.). This course will be under the Yashwantrao Chavan Open
								University, Nashik, and will be helpful to those students who
								have passed out degree course from this center and other.</p></li>
