				<h3 class="margin-bottom-10">
					<b> Brief History </b>
				</h3>
				<p style="font-size: 16px; text-align: justify">
					Student is a soul and Library is a heart of an educational
					institute. The progress and quality depends on these two important
					aspects. To cater the educational and intellectual needs of the
					readers, our college established the library department in the year
					1965. It had a collection of 2940 books with sufficient number of
					text books &amp; other reference books. At the beginning, the
					strength of the students was 270, teachers were 26 and 17 was the
					number of the non-teaching staff. From this minimum number, the
					library is enlarging with its readers, collection, space and staff.<br>
					Book-Bank facility for poor and needy students viz. Vidyarthi
					Kalyan (VK) Mandal started in the year 1966-1967, and B.C. Book
					Bank facility for B.C. Students from Mumbai University in 1982-83.<br>
					<b>In 1992-93, on the occasion of Dr. S.R. Ranganathan's birth
						anniversary programme, following dignitaries visited the library
						&amp; expressed satisfaction.</b><br>
				</p>
				<ol style="font-size: 16px">
					<li>Dr. Shrirang Kadrekar, V.C. Konkan Agriculture University,
						Dapoli.<br>
					</li>
					<li>Prof. A.C. Tikekar, Head Librarian, Mumbai University.<br></li>
					<li>Prof. S.G. Mahajan, Librarian, Pune University.<br></li>
					<li>Shri M.M. Ujlambkar, Retired Library Director, Maharashtra
						State.<br>
					</li>
				</ol>
				<p style="font-size: 16px; text-align: justify">
					The library computerization was completed in the year 2001 and was
					inaugurated on 7th October 2001 by Dr. Bhalchandra Munagekar, the
					Vice Chancellor of Mumbai University. Office clerk Shri. Vijay
					Jadhav and Miss. Shirish Sardesai from Computer Science Department
					were felicitated by the V.C. for their invaluable work in the
					computerization of the library. In the same year Mrs. R.V. Kadam
					was appointed as a library clerk.<br> In 2003, Library was
					renovated to provide an efficient, student friendly arrangement. In
					this year, Library got its own building, in the present form. Home
					lending started at the ground floor. Stack room was shifted to
					second floor. Internet facility for students and staff also started
					at the second floor. This year, NAAC peer team visited the library,
					and expressed their satisfaction. The figure of books reached upto
					29692.<br> From the year, 2004-2005, a degree course in Library and
					Information Science started with collaboration of YCMOU, Nashik.
					The student of this first batch, Mr. Rahul Barve got first rank in
					the university and facilitated with Dr. S.G. Mahajan Gold Medal.<br>
					On 15th and 16th October, 2010, Library had organized a two days
					state level workshop on "Koha" (an open source library management
					software). The workshop was inaugurated by Mr. Prakash Deshpande.
					There were 50 participants from various corner of the state,
					attended t he workshop. From this year, library opted Koha and
					Dspace software for its automation and digital library.<br> In the
					year 2011, library subscribed a digital online resource for its
					users viz. INFLIBNET N-LIST online consortia. Through this
					consortia, users of library can access more than 4100 e-journals
					and 93000 e-books, with the help of login and password, provided by
					the library. The collection of the library reached upto 55687
					books.<br>
