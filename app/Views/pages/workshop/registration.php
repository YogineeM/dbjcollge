					  var res=JSON.parse(data);
					  } catch (e) {					  
					  }					  
				  if(res["status"]){
					  alert(" success");
					  Swal.fire(
							  'Thank You!',
							  res["message"],
							  'success'
							);
					  $("#ms").css("display", "block");
					  $("#mf").css("display", "none");
