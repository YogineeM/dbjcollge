<script type="text/javascript">

function verify(){

	  $("#ajax").css("visibility", "visible");
	var name=$("#name").val();
	var level=$("#level").val();
	var dob=$("#dob").val();
	var cno=$("#cno").val();
	var cdate=$("#cdate").val();
	var rname=$("#rname").val();
	var remail=$("#remail").val();
	var reason=$("#reason").val();
	$.post("http://localhost/abs_kaizen/public/verify-certificate",
			  {
				name:name,
				level:level,
				dob:dob,
				cno:cno,
				cdate:cdate,
				reason:reason,
				rname:rname,
				remail:remail,
				medium:'WEB',
				ip:''
			  },
			  function(data, status){
				 
				  try {
					  res=JSON. parse(data);
					  } catch (e) {
					 
					  }
					  
				  if(res["status"]){
					  Swal.fire(
							  'Thank You!',
							  res["message"],
							  'success'
							);
					  $("#ms").css("display", "block");
					  $("#mf").css("display", "none");
					  $("#ajax").css("visibility", "hidden");
					  document.getElementById('vf').reset();
				  }else{
					  $("#mf").css("display", "block");
					  $("#ms").css("display", "none");
					  $("#ajax").css("visibility", "hidden");
				  }
			  });
	return false;
}

</script>