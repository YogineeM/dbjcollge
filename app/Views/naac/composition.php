									<h3>Composition</h3>
								</div>
								<div class="description-text">
									<div class="row">

										<div class="col-sm-12" style="">

											<p class="text-black"
												style="line-height: 35px;  font-family: 'Roboto', sans-serif; font-size: 16px">


												IQAC may be constituted in every institution under the
												Chairmanship of the Head of the institution with heads of
												important academic and administrative units and a few
												teachers and a few distinguished educationists and
												representatives of local management and stakeholders. The
												composition of the IQAC may be as follows:</p>

											<ul class="list-unstyled list-services"
												style="padding-left: 20px; font-size: 16px; font-family: 'Roboto', sans-serif; line-height: 35px">
												<li><i class="fa fa-chevron-circle-right color-gray-light"></i>&nbsp;Chairperson:
													Head of the Institution</li>
												<li><i class="fa fa-chevron-circle-right color-gray-light"></i>&nbsp;Teachers
													to represent all level (Three to eight)</li>
												<li><i class="fa fa-chevron-circle-right color-gray-light"></i>
													&nbsp;One member from the Management</li>
												<li><i class="fa fa-chevron-circle-right color-gray-light"></i>&nbsp;Few
													Senior administrative officers</li>
												<li><i class="fa fa-chevron-circle-right color-gray-light"></i>&nbsp;One
													nominee each from local society, Students and Alumni</li>
												<li><i class="fa fa-chevron-circle-right color-gray-light"></i>&nbsp;One
													nominee each from Employers /Industrialists/Stakeholders</li>
												<li><i class="fa fa-chevron-circle-right color-gray-light"></i>&nbsp;One
													of the senior teachers as the coordinator/Director of the
													IQAC</li>

											</ul>



											<p class="text-black"
												style="line-height: 35px;  font-family: 'Roboto', sans-serif; font-size: 16px">
												The composition of the IQAC will depend on the size and
												complexity of the institution, accordingly the
												representation of teachers may vary. It helps the
												institutions in planning and monitoring. IQAC also gives
												stakeholders or beneficiaries a cross-sectional
												participation in the institution's quality enhancement
												activities. The guidelines given here are only indicative
												and will help the institutions for quality sustenance
												activities. The membership of such nominated members shall
												be for a period of two years. The IQAC should meet at least
												once in every quarter. The quorum for the meeting shall be
												two-third of the total number of members. The agenda,
												minutes and Action Taken Reports are to be documented with
												official signatures and maintained electronically in a
												retrievable format. It is necessary for the members of the
												IQAC to shoulder the responsibilities of generating and
												promoting awareness in the institution and to devote time
												for working out the procedural details. While selecting
												these members several precautions need to be taken. A few of
												them are listed below:</p>
											<p class="text-black"
												style="line-height: 35px;  font-family: 'Roboto', sans-serif; font-size: 16px">
												It is advisable to choose persons from various backgrounds
												who have earned respect for integrity and excellence in
												their teaching and research. Moreover, they should be aware
												of the ground realities of the institutional environment.
												They should be known for their commitment to improving the
												quality of teaching and learning.</p>
											<p class="text-black"
												style="line-height: 35px;  font-family: 'Roboto', sans-serif; font-size: 16px">
												It is advisable to change the co-ordinator after two to
												three years to bring new thoughts and activities in the
												institution.</p>
											<p class="text-black"
												style="line-height: 35px;  font-family: 'Roboto', sans-serif; font-size: 16px">
												It would be appropriate to choose as senior administrators,
												persons in charge of institutional services such as library,
												computer center, estate, student welfare, administration,
												academic tasks, examination and planning and development.</p>
											<p class="text-black"
												style="line-height: 35px;  font-family: 'Roboto', sans-serif; font-size: 16px">
												The management representative should be a person who is
												aware of the institution's objectives, limitations and
												strengths and is committed to its improvement. The local
												society representatives should be of high social standing
												and should have made significant contributions to society
												and in particular to education.</p>

