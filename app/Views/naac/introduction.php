												In pursuance of its Action Plan for performance evaluation,
												assessment and accreditation and quality up-gradation of
												institutions of higher education, the National Assessment
												and Accreditation Council (NAAC), Bangalore proposes that
												every accredited institution should establish an Internal
												Quality Assurance Cell (IQAC) as a quality sustenance
												measure. Since quality enhancement is a continuous process,
												the IQAC will become a part of the institution's system and
												work towards realisation of the goals of quality enhancement
												and sustenance. The prime task of the IQAC is to develop a
												system for conscious, consistent and catalytic improvement
												in the overall performance of institutions. For this, during
												the post-accreditation period, institutions need to
												channelize its efforts and measures towards promoting the
												holistic academic excellence including the peer committee
												recommendations. The guidelines provided in the following
												pages will guide and facilitate the institution in the
												creation and operation of the Internal Quality Assurance
												Cell (IQAC). The work of the IQAC is the first step towards
												internalization and institutionalization of quality
												enhancement initiatives. Its success depends upon the sense
												of belongingness and participation in all the constituents
												of the institution. It will not be yet another hierarchical
												structure or a record-keeping exercise in the institution.
												It will be a facilitative and participative voluntary
												system/unit/organ of the institution. It has the potential
												to become a vehicle for ushering in quality enhancement by
												working out planned interventionist strategies by IQAC to
												remove deficiencies and enhance quality like the "Quality
												Circles" in industries.</p>
