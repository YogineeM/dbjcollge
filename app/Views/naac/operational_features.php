												style="padding-left: 20px; font-size: 16px; font-family: 'Roboto', sans-serif; line-height: 35px">
												<li><i class="fa fa-chevron-circle-right color-gray-light"></i>Having
													a functional IQAC.</li>
												<li><i class="fa fa-chevron-circle-right color-gray-light"></i>The
													minutes of IQAC meeting and compliance to the decisions
													should be uploaded on the institutional website.</li>
												<li><i class="fa fa-chevron-circle-right color-gray-light"></i>Mandatory
													submission of AQARs on a regular basis for institutions
													undergoing the second and subsequent cycles of Assessment
													and Accreditation by NAAC.</li>
												<li><i class="fa fa-chevron-circle-right color-gray-light"></i>Upload
													the AQAR's on institutional website for access to all
													stakeholders.</li>
