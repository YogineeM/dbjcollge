									<h3>Operational Features</h3>
								</div>
								<div class="description-text">
									<div class="row">

										<div class="col-sm-12" style="text-align: justify;">

											<p class="text-black"
												style="line-height: 35px; font-size: 15px; font-family: 'Roboto', sans-serif; font-size: 16px">


												Quality assurance is a by-product of ongoing efforts to
												define the objectives of an institution, to have a work plan
												to achieve them and to specify the checks and balances to
												evaluate the degree to which each of the tasks is fulfilled.
												Hence devotion and commitment to improvement rather than
												mere institutional control is the basis for devising
												procedures and instruments for assuring quality. The right
												balance between the health and growth of an institution
												needs to be struck. The IQAC has to ensure that whatever is
												done in the institution for "education" is done efficiently
												and effectively with high standards. In order to do this,
												the IQAC will have to first establish procedures and
												modalities to collect data and information on various
												aspects of institutional functioning.</p>



											<p class="text-black"
												style="line-height: 35px; font-size: 15px; font-family: 'Roboto', sans-serif; font-size: 16px">
												The coordinator of the IQAC will have a major role in
												implementing these functions. The IQAC may derive major
												support from the already existing units and mechanisms that
												contribute to the functions listed above. The operational
												features and functions discussed so far are broad-based to
												facilitate institutions towards academic excellence and
												institutions may adapt them to their specific needs.</p>

											<p class="text-black"
												style="line-height: 35px; font-size: 15px; font-family: 'Roboto', sans-serif; font-size: 16px">
												The institutions need to submit yearly the Annual Quality
												Assurance Report (AQAR) to NAAC by end of September every
												year positively. A functional Internal Quality Assurance
												Cell (IQAC) and timely submission of Annual Quality
												Assurance Reports (AQARs) are the Minimum Institutional
												Requirements (MIR) to volunteer for second, third or
												subsequent cycle's accreditation. During the institutional
												visit the NAAC peer teams will interact with the IQACs to
												know the progress, functioning as well as quality sustenance
												initiatives undertaken by them.</p>

											<p class="text-black"
												style="line-height: 35px; font-size: 15px; font-family: 'Roboto', sans-serif; font-size: 16px">
												The Annual Quality Assurance Report (AQAR) may be the part
												of the Annual Report. The AQAR shall be approved by the
												statutory bodies of the HEIs (such as Governing Council/
												Executive Council/Board of Management) for the follow up
												action for necessary quality enhancement measures.</p>

											<p class="text-black"
												style="line-height: 35px; font-size: 15px; font-family: 'Roboto', sans-serif; font-size: 16px">

												The IQACs may create its exclusive window tab on its
												institutional website for keeping the records/files of NAAC,
												Peer Team Reports, AQAR, and Certificate of Accreditation
												Outcomes and regularly upload/ report on its activities, as
												well as for hosting the AQAR.</p>

											<h4>Revised Accreditation Framework</h4>
											<p class="text-black"
												style="line-height: 35px; font-size: 15px; font-family: 'Roboto', sans-serif; font-size: 16px">
												NAAC has launched Revised Accreditation Framework since
												July, 2017 and hence AQAR format also modified, in
												cognizance with the new methodology. The tools and
												parameters are designed in the new AQAR format are in such a
												way that the preparation of AQAR would facilitate the HEI's
												for upcoming cycles of Accreditation. Data
												collected/prepared infuses quality enhancement measures
												undertaken during the years. Further, it also adds quality
												enhancement and quality sustenance measures undertaken in
												teaching, learning, research, extension and support
												activities of the Institution. It is hoped that new AQAR
												would facilitate Educational Institutions for creating a
												good database at Institutional level for enhancing the
												quality culture.</p>
											<p class="text-black"
												style="line-height: 35px; font-size: 15px; font-family: 'Roboto', sans-serif; font-size: 16px">
												The Higher Education Institutions need not submit the
												printed/hard copy of AQAR to NAAC. Mandatory Submission of
												AQAR by IQAC</p>

											<p class="text-black"
												style="line-height: 35px; font-size: 15px; font-family: 'Roboto', sans-serif; font-size: 16px">
												The Executive Committee of NAAC has decided that regular
												submission of AQARs is mandatory for 2nd and subsequent
												cycles of accreditation with effect from 16th September
												2016: The following are the pre-requisites for submission of
												IIQA for all Higher Education Institutions (HEI's) opting
												for 2nd and subsequent cycles of A& A:</p>


